package chap1_2;

public class Math {

	public static void main(String[] args) {
		int a = 1000;
		int b = 2000;
		System.out.println("PLUS= "+ (a+b));
		System.out.println("MINUS= "+ (a-b));
		System.out.println("MULTIPLE= "+ (a*b));
		System.out.printf("DIVISION= %.7f",(double)a/b);
		// 소수점 아래 7자리까지 출력
		
	}

}
